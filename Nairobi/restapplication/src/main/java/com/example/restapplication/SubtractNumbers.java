package com.example.restapplication;

public class SubtractNumbers {
	
	private Integer a;
	private Integer b;
	
	public SubtractNumbers(Integer a, Integer b)
	{
		this.a = a;
		this.b = b;
	}
	
	public Integer getFirstNumber()
	{
		return this.a;
	}
	
	public Integer getSecondNumber()
	{
		return this.b;
	}
}
